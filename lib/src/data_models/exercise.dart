enum ResistanceCurveEntry { stretch, middle, contraction }

enum FatigueRating {
  low, // low fatigue (isolation) - single muscle exercise
  moderate, // moderate fatigue (stable compound) - multiple muscles trained but weights very stabilised (usually machines)
  high, // high fatigue (compound) - multiple muscles trained with free weights
  veryHigh // very high fatigue (unstable compound) - multiple muscles trained across multiple joints with free weights often requiring stabilisation
}

enum SelectionPreference { doNotSelect, ifNoOtherOption, prefer, ideal }

class ExerciseSet {
  final double weight;
  final int reps;
  final bool completed;

  const ExerciseSet({
    required this.weight,
    required this.reps,
    this.completed = false,
  });

  ExerciseSet copyWith(
      {double? weight, int? reps, bool? toggleCompleted, bool? completed}) {
    return ExerciseSet(
      weight: weight ?? this.weight,
      reps: reps ?? this.reps,
      completed: completed ?? toggleCompleted == null || !toggleCompleted
          ? this.completed
          : !this.completed,
    );
  }

  factory ExerciseSet.fromMap(
    Map<String, dynamic> map,
  ) {
    final completed = map['completed'];
    return ExerciseSet(
      weight: map['weight'] + .0,
      reps: map['reps'] as int,
      completed: completed != null ? completed as bool : false,
    );
  }

  factory ExerciseSet.from(
    ExerciseSet exerciseSet,
  ) {
    return ExerciseSet(
      weight: exerciseSet.weight,
      reps: exerciseSet.reps,
      completed: exerciseSet.completed,
    );
  }

  Map<String, dynamic> toJson() => {
        'weight': weight,
        'reps': reps,
        'completed': completed,
      };
}

class Exercise {
  final String id;
  final String name;
  final String shortName;
  final ExerciseType type;
  final ExerciseMuscleGroup muscleGroup;
  final Map<ExerciseMuscleGroup, double> musclesTrainedPerSet;
  final bool unilateral;
  final Map<ResistanceCurveEntry, double> resistanceCurve;
  final FatigueRating fatigueRating;
  final SelectionPreference preference;
  List<ExerciseSet> sets;
  DateTime? date;

  Exercise({
    required this.id,
    required this.name,
    required this.shortName,
    required this.type,
    required this.muscleGroup,
    required this.musclesTrainedPerSet,
    required this.unilateral,
    required this.resistanceCurve,
    required this.fatigueRating,
    required this.preference,
    this.sets = const [
      ExerciseSet(weight: 5.0, reps: 10),
      ExerciseSet(weight: 5.0, reps: 10),
      ExerciseSet(weight: 5.0, reps: 10)
    ],
    this.date,
  });

  void setSetCount(int count) {
    if (count < 1) {
      return;
    }

    if (count < sets.length) {
      sets = sets.sublist(0, count);
      return;
    }

    sets = [
      ...sets,
      ...List.generate(
        count - sets.length,
        (index) => ExerciseSet(weight: sets[0].weight, reps: sets[0].reps),
      ),
    ];
  }

  void setSetReps(int reps) {
    sets = sets.map((exerciseSet) => exerciseSet.copyWith(reps: reps)).toList();
  }

  factory Exercise.fromMap(
    Map<String, dynamic> map,
  ) {
    final id = map['id'] as String;
    final templateExercise = Exercise.fromId(id);

    return Exercise(
      id: id,
      name: templateExercise.name,
      shortName: templateExercise.shortName,
      type: templateExercise.type,
      muscleGroup: templateExercise.muscleGroup,
      musclesTrainedPerSet: templateExercise.musclesTrainedPerSet,
      unilateral: templateExercise.unilateral,
      resistanceCurve: templateExercise.resistanceCurve,
      fatigueRating: templateExercise.fatigueRating,
      preference: templateExercise.preference,
      sets: (map['sets'] as List)
          .map((exerciseSet) =>
              ExerciseSet.fromMap(exerciseSet as Map<String, dynamic>))
          .toList(),
    );
  }

  Map<String, dynamic> toJson() => {
        'id': id,
        'sets': sets,
      };

  factory Exercise.fromId(String id) {
    return _allExercises[id]!;
  }

  factory Exercise.from(Exercise exercise) {
    return Exercise(
      id: exercise.id,
      name: exercise.name,
      shortName: exercise.shortName,
      type: exercise.type,
      muscleGroup: exercise.muscleGroup,
      musclesTrainedPerSet: exercise.musclesTrainedPerSet,
      unilateral: exercise.unilateral,
      resistanceCurve: exercise.resistanceCurve,
      fatigueRating: exercise.fatigueRating,
      preference: exercise.preference,
      sets: exercise.sets.map((s) => ExerciseSet.from(s)).toList(),
      date: exercise.date,
    );
  }

  int getRestTime() {
    return switch (fatigueRating) {
      FatigueRating.low => 60,
      FatigueRating.moderate => 120,
      FatigueRating.high => 180,
      FatigueRating.veryHigh => 210
    };
  }

  static List<Exercise> search(String query) {
    return _allExercises.values
        .where((exercise) =>
            exercise.name.toLowerCase().contains(query.toLowerCase()))
        .toList();
  }

  static bool checkExerciseUniqueness() {
    bool isUnique = true;
    Set<String> exerciseNames = {};
    for (final exercise in _allExercises.values) {
      if (exerciseNames.contains(exercise.name)) {
        print('Duplicate exercise: ${exercise.name}');
        isUnique = false;
      }
      exerciseNames.add(exercise.name);
    }
    return isUnique;
  }

